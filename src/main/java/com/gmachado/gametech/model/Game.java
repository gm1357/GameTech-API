package com.gmachado.gametech.model;

public class Game {
    private String name;
    private String deck;
    private String cover;
    private String guid;

    public String getName() {
        return name;
    }

    public String getDeck() {
        return deck;
    }

    public String getCover() {
        return cover;
    }

    public String getGuid() {
        return guid;
    }
}
