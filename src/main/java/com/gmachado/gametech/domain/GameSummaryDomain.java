package com.gmachado.gametech.domain;

public class GameSummaryDomain {
    private String name;
    private String deck;
    private ImageDomain image;
    private String guid;

    public String getName() {
        return name;
    }

    public String getDeck() {
        return deck;
    }

    public ImageDomain getImage() {
        return image;
    }

    public String getGuid() {
        return guid;
    }
}
